public class TypeCasting {
    public static void main(String[] args) {
        //type casting

        //implicit
        int n=5;
        double d=n;
        System.out.println(d);

        // explicit
        double d1=9.8;
        int n1=d1;
        System.out.println(n1);

        int n3=200;
        byte b=(byte)n3;
        System.out.println(b);

    }
}
