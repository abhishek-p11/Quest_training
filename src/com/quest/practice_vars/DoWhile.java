package com.quest.practice_vars;

public class DoWhile {
    public static void main(String[] args) {
        int i=0;
        int j=7;
        do{
            System.out.println("print statment");
            i++;
        }while(i<0);
        do{
            System.out.println(j);
            j--;
        }while(j>0);
    }

}
