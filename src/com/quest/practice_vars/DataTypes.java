package com.quest.practice_vars;

public class DataTypes {
    public static void main(String[] args) {
        byte  n=120; //-128 to 127 -(1 byte)
        int a=10; //(2 byte)
        long b=9876596879L; //(8 byte)
        float c=3.14f; //6 desimal digits (4 bytes)
        double d=3.87095335; //15 desimal digits (8 bytes)
        char e='d'; // (2 byte)
        boolean f=true; //(1 byte)
        String g="hello world";
        short h=123; //-32768 to 32767 -(2 byte)
    }
}
